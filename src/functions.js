        let http = require('http');
        let fs = require(`fs`);

        // DOWNLOAD LOG FILE
        function download(url, id){
            if (!fs.existsSync("./logs")) {
                fs.mkdirSync("./logs");
            }
            http.get(url, (result) => {
                result.on("error", (err) => console.error(err));
                result.pipe(fs.createWriteStream("./logs/" + id + ".txt"));
            });
            console.log(`Processing a log file`);
        }
        
